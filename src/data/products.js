/* ─── Helper : retrouver n'importe quel produit par son id ─── */
export function getProductById(id) {
  return (
    [...produitsFilaire, ...produitsIntegre, ...produitsAccessoires].find((p) => p.id === id) ?? null
  )
}

/* ─── Produits liés (même type, hors produit courant) ─── */
export function getRelatedProducts(product, max = 4) {
  const pool =
    product.type === 'filaire'    ? produitsFilaire    :
    product.type === 'integre'    ? produitsIntegre    :
    produitsAccessoires
  return pool.filter((p) => p.id !== product.id).slice(0, max)
}
